public class PrimitivePractice {
    public static void main(String[] args) {
        int age = 18;
        double price = 9.99;
        boolean isStudent = true;
        char grade = 'A';

        System.out.println("age = " + age);
        System.out.println("int stores whole numbers.");
        System.out.println();

        System.out.println("price = " + price);
        System.out.println("double stores decimal numbers.");
        System.out.println();

        System.out.println("isStudent = " + isStudent);
        System.out.println("boolean stores true or false.");
        System.out.println();

        System.out.println("grade = " + grade);
        System.out.println("char stores one character.");
    }
}
